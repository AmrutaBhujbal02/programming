#SYNTAX OF ELEMENTS SLICING IN LIST
#list_name=[start:stop]

letters=['a','b','c','d','e']

print("elements from starting to end of list:")
print(letters[0:])

print("elements from starting to end of list:")
print(letters[:5])


print("elements from 'a' to 'c':")
print(letters[0:2])


print("to reverse to list:")
print(letters[::-1])

print("to print element from 'a' to direct 'd':")
print(letters[::3])

print("if you give step size as 2 see what will happen:")
print(letters[::2])

